package Part1UsingVariables;

public class CalculatingWithRemainders {

	public static void main(String[] args) {
		
		/*
		 * If you notice, the remainder is not being displayed
		 * because int does not display decimal point
		 * So you have to put it as double
		 */
		System.out.println(9/3);
		System.out.println(10/3);
		
		/*
		 * This outputs the remainder as it has the proper format
		 */
		System.out.println(10.0/3.0);
	}
}
